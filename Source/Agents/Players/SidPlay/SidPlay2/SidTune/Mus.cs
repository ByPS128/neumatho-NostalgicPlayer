		/********************************************************************/
		/// <summary>
		/// Will test the file to see if it's a MUS/STR file
		/// </summary>
		/********************************************************************/
		private bool TestMus(ModuleStream moduleStream)
		{
			// Skip load address and 3x length entry
			uint voice1Index = 2 + 3 * 2;

			// Add length of voice 1 data
			moduleStream.Seek(2, SeekOrigin.Begin);
			voice1Index += moduleStream.Read_L_UINT16();

			// Add length of voice 2 data
			uint voice2Index = voice1Index + moduleStream.Read_L_UINT16();

			// Add length of voice 3 data
			uint voice3Index = voice2Index + moduleStream.Read_L_UINT16();

			if (voice3Index > moduleStream.Length)
				return false;

			moduleStream.Seek(voice1Index - 2, SeekOrigin.Begin);
			if (moduleStream.Read_B_UINT16() != MusHltCmd)
				return false;

			moduleStream.Seek(voice2Index - 2, SeekOrigin.Begin);
			if (moduleStream.Read_B_UINT16() != MusHltCmd)
				return false;

			moduleStream.Seek(voice3Index - 2, SeekOrigin.Begin);
			if (moduleStream.Read_B_UINT16() != MusHltCmd)
				return false;

			return true;
		}



		/********************************************************************/
		/// <summary>
		/// Will return the index to the info strings
		/// </summary>
		/********************************************************************/
		private int FindInfoTextIndex(byte[] buf)
		{
			uint voice1Index = 2 + 3 * 2;
			voice1Index += Endian.Endian16(buf[3], buf[2]);
			uint voice2Index = voice1Index + Endian.Endian16(buf[5], buf[4]);
			uint voice3Index = voice2Index + Endian.Endian16(buf[7], buf[6]);

			return (int)voice3Index;
		}



		/********************************************************************/
		/// <summary>
		/// Will load the file as a MUS file
		/// </summary>
		/********************************************************************/
		private bool LoadMus(byte[] musBuf, byte[] strBuf, byte[] wdsBuf, out string errorMessage)
		{
			errorMessage = string.Empty;

			info.Songs = info.StartSong = 1;
			info.MusPlayer = true;

			songSpeed[0] = Speed.Cia_1A;
			clockSpeed[0] = Clock.Any;

			// Check setting compatibility for MUS playback
			if ((info.Compatibility != Compatibility.C64) || (info.RelocStartPage != 0) || (info.RelocPages != 0))
			{
				errorMessage = Resources.IDS_SID_ERR_INVALID_DATA;
				return false;
			}

			// All sub-tunes should be CIA
			for (ushort i = 0; i < info.Songs; i++)
			{
				if (songSpeed[i] != Speed.Cia_1A)
				{
					errorMessage = Resources.IDS_SID_ERR_INVALID_DATA;
					return false;
				}
			}

			musDataLen = (uint)musBuf.Length;
			info.LoadAddr = MusDataAddr;
			info.SidChipBase1 = Sid1BaseAddr;

			// Voice3Index now is offset to text lines (uppercase Pet-strings)
			int pet = FindInfoTextIndex(musBuf);

			// Extract credits
			info.Comment = GetPetsciiStrings(musBuf, pet, musBuf.Length - pet, false);

			// If second buffer is supplied, extract credits from there as well
			if (strBuf != null)
			{
				pet = FindInfoTextIndex(strBuf);

				info.Comment.AddRange(GetPetsciiStrings(strBuf, pet, strBuf.Length - pet, false));

				info.SidChipBase2 = Sid2BaseAddr;
				info.FormatString = Resources.IDS_SID_FORMAT_STR;
			}
			else
			{
				info.SidChipBase2 = 0;
				info.FormatString = Resources.IDS_SID_FORMAT_MUS;
			}

			// If lyrics is supplied, extract them
			info.Lyrics = wdsBuf != null ? GetPetsciiStrings(wdsBuf, 0, wdsBuf.Length, true) : new List<string>();

			SetPlayerAddress();

			RemoveTrailingEmptyLines(info.Comment);
			RemoveTrailingEmptyLines(info.Lyrics);

			return true;
		}



		/********************************************************************/
		/// <summary>
		/// Convert the comment to the right unicode characters
		/// </summary>
		/********************************************************************/
		private List<string> GetPetsciiStrings(byte[] buf, int index, int length, bool lyrics)
		{
			List<string> result = new List<string>();
			char[] tempBuffer = new char[length];

			ushort[] lowerLookupTable = lyrics ? chrTabUpper : chrTabLower;
			ushort[] upperLookupTable = lyrics ? chrTabLowerLyrics : chrTabUpper;

			ushort[] lookupTable = upperLookupTable;
			bool reversed = false;

			while (length > 0)
			{
				int destIndex = 0;

				for (; length > 0;)
				{
					byte b = buf[index++];
					length--;

					// Handle special characters
					if ((b == 0x00) || (b == 0x0d))
						break;

					switch (b)
					{
						// Lower case
						case 0x0e:
						{
							lookupTable = lowerLookupTable;
							break;
						}

						// Upper case
						case 0x8e:
						{
							lookupTable = upperLookupTable;
							break;
						}

						// Reverse on
						case 0x12:
						{
							reversed = true;
							break;
						}

						// Reverse off
						case 0x92:
						{
							reversed = false;
							break;
						}

						// Cursor left
						case 0x9d:
						{
							if (destIndex > 0)
								destIndex--;

							break;
						}
					}
					
					ushort v = lookupTable[b];
					if (v != 0x0001)
					{
						if (reversed)
							v += 0x80;

						tempBuffer[destIndex++] = (char)v;
					}
				}

				result.Add(tempBuffer.AsSpan(0, destIndex).ToString());
			}

			return result;
		}



		/********************************************************************/
		/// <summary>
		/// Remove trailing empty lines
		/// </summary>
		/********************************************************************/
		private void RemoveTrailingEmptyLines(List<string> lines)
		{
			if (lines.Count > 0)
			{
				for (int line = info.Comment.Count - 1; line >= 0; line--)
				{
					if (string.IsNullOrEmpty(lines[line]))
						lines.RemoveAt(line);
					else
						break;
				}
			}
		}



		/********************************************************************/
		/// <summary>
		/// Will merge the two files together
		/// </summary>
		/********************************************************************/
		private bool MergeParts(byte[] musBuf, byte[] strBuf, out byte[] newBuf, out string errorMessage)
		{
			errorMessage = string.Empty;
			newBuf = null;

			int mergeLen = musBuf.Length;
			if (strBuf != null)
				mergeLen += strBuf.Length;

			// Sanity check. I do not trust those MUS/STR files around
			int freeSpace = Endian.Endian16(sidPlayer1[1], sidPlayer1[0]) - MusDataAddr;

			if ((mergeLen - 4) > freeSpace)
			{
				errorMessage = Resources.IDS_SID_ERR_SIZE_EXCEEDED;
				return false;
			}

			byte[] mergeBuf = new byte[mergeLen];

			// Install MUS data #1 including load address
			Array.Copy(musBuf, 0, mergeBuf, 0, musBuf.Length);

			if ((strBuf != null) && (info.SidChipBase2 != 0))
			{
				// Install MUS data #2 NOT including load address
				Array.Copy(strBuf, 0, mergeBuf, musBuf.Length, strBuf.Length);
			}

			newBuf = mergeBuf;

			return true;
		}



		/********************************************************************/
		/// <summary>
		/// Set the init and play address to the right player
		/// </summary>
		/********************************************************************/
		private void SetPlayerAddress()
		{
			if (info.SidChipBase2 == 0)
			{
				// Player #1
				info.InitAddr = 0xec60;
				info.PlayAddr = 0xec80;
			}
			else
			{
				// Player #1 + #2
				info.InitAddr = 0xfc90;
				info.PlayAddr = 0xfc96;
			}
		}



		/********************************************************************/
		/// <summary>
		/// Install the players in the C64 memory
		/// </summary>
		/********************************************************************/
		private void InstallMusPlayer(byte[] c64Buf)
		{
			if (status && (c64Buf != null))
			{
				// Install MUS player #1
				ushort dest = Endian.Endian16(sidPlayer1[1], sidPlayer1[0]);
				Array.Copy(sidPlayer1, 2, c64Buf, dest, sidPlayer1.Length - 2);

				// Point player #1 to data #1
				c64Buf[dest + 0xc6e] = (MusDataAddr + 2) & 0xff;
				c64Buf[dest + 0xc70] = (MusDataAddr + 2) >> 8;

				if (info.SidChipBase2 != 0)
				{
					// Install MUS player #2
					dest = Endian.Endian16(sidPlayer2[1], sidPlayer2[0]);
					Array.Copy(sidPlayer2, 2, c64Buf, dest, sidPlayer2.Length - 2);

					// Point player #2 to data #2
					c64Buf[dest + 0xc6e] = (byte)((MusDataAddr + musDataLen + 2) & 0xff);
					c64Buf[dest + 0xc70] = (byte)((MusDataAddr + musDataLen + 2) >> 8);
				}
			}
		}
